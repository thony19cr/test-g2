
"""Tipo de datos básicos"""

"""Flotantes: Números decimales"""

real = 1.1 + 2.2

print("Valor de mi variable tipo flotante: ", real)

print(f"{real:.5f}")

"""Conocer el tipo de datos de mi variable"""

print("El tipo de dato de mi variable 'real' es:", type(real))


"""Conversión de tipo de datos"""

print("Conversión de tipo flotante a entero: ", int(real))
