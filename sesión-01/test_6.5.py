
"""Operacion aritméticas con python"""

"""Operador Potencia: **"""

potencia1 = 10**3

"""Usando la palabra reservada 'pow' podemos también elevar a un cierta portencia un número"""
"""Nos pedirá 2 valores, el primero es la base y el segundo es la potencia a ser elevada"""

potencia2 = pow(20, 4)
potencia3 = pow(8, 4)

print("El valor de nuestra potencia1 es: ", potencia1)
print("El valor de nuestra potencia2 es: ", potencia2)

print("El valor de nuestra potencia3 es: ", potencia3)
