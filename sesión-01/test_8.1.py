
"""Diccionarios"""

miDiccionario = {'nombre': 'Python', 'tipo': 'Backend'}

print("Mi diccionario: ", miDiccionario)

"""Para eliminar valores de nuestro diccionario usamos: del"""

del miDiccionario["tipo"]

print("Mi diccionario final es: ", miDiccionario)

del miDiccionario["nombre"]

print("Mi diccionario final es: ", miDiccionario)
