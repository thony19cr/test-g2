
"""Uso del flujo condicional if"""

var1 = int(input("Ingrese su primer valor: "))
var2 = int(input("Ingrese su segundo valor: "))

if var1 >= var2: # Si es verdadero ejecuta su l√≥gica interna del flujo condicional
    print("El valor de var1 es mayor que la segunda variable")
    print("Es verdero")
else:
    print("El valor de var1 no es mayor que la segunda variable")
    print("La sentencia es falsa")
